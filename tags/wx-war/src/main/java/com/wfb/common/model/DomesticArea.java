package com.wfb.common.model;

public class DomesticArea {

	private Integer fId;

	private String fName;

	private String fNid;

	private Integer fPid;

	private String fDomain;

	private Short fOrder;

	private String fAreaCode;

	public Integer getfId() {
		return fId;
	}

	public void setfId(Integer fId) {
		this.fId = fId;
	}

	public String getfName() {
		return fName;
	}

	public void setfName(String fName) {
		this.fName = fName;
	}

	public String getfNid() {
		return fNid;
	}

	public void setfNid(String fNid) {
		this.fNid = fNid;
	}

	public Integer getfPid() {
		return fPid;
	}

	public void setfPid(Integer fPid) {
		this.fPid = fPid;
	}

	public String getfDomain() {
		return fDomain;
	}

	public void setfDomain(String fDomain) {
		this.fDomain = fDomain;
	}

	public Short getfOrder() {
		return fOrder;
	}

	public void setfOrder(Short fOrder) {
		this.fOrder = fOrder;
	}

	public String getfAreaCode() {
		return fAreaCode;
	}

	public void setfAreaCode(String fAreaCode) {
		this.fAreaCode = fAreaCode;
	}
}
